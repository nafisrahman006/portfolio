const CW=130, RH=100, NW=100, NH=48, PX=20, PY=24

function pos(n) {
  return { cx: PX + n.x*CW + NW/2, cy: PY + n.y*RH + NH/2 }
}

export default function PipelineBg() {
  const [step, setStep] = useState(0)

  useEffect(() => {
    const t = setInterval(() => setStep(s => (s+1) % EDGES.length), 900)
    return () => clearInterval(t)
  }, [])

  const svgW = PX*2 + 7*CW + NW
  const svgH = PY*2 + 2*RH + NH

  const getNode = id => NODES.find(n => n.id === id)

  return (
    <div className="pipeline-bg" aria-hidden="true">
      <svg
        viewBox={`0 0 ${svgW} ${svgH}`}
        className="pipeline-bg-svg"
        xmlns="http://www.w3.org/2000/svg"
        preserveAspectRatio="xMidYMid meet"
      >
        <defs>
          <marker id="bg-arr" markerWidth="5" markerHeight="5" refX="4" refY="2.5" orient="auto">
            <path d="M0,0 L5,2.5 L0,5 z" fill="rgba(0,212,255,0.6)" />
          </marker>
          <marker id="bg-arr-on" markerWidth="5" markerHeight="5" refX="4" refY="2.5" orient="auto">
            <path d="M0,0 L5,2.5 L0,5 z" fill="#00d4ff" />
          </marker>
        </defs>

        {/* Edges */}
        {EDGES.map((e, i) => {
          const f = pos(getNode(e.from))
          const t = pos(getNode(e.to))
          const isOn = step === i
          const isVert = Math.abs(f.cy - t.cy) > Math.abs(f.cx - t.cx)
          const mx = (f.cx+t.cx)/2, my = (f.cy+t.cy)/2
          const cx2 = isVert ? f.cx+10 : mx
          const cy2 = isVert ? my : f.cy - 10
          return (
            <g key={i}>
              <path
                d={`M${f.cx},${f.cy} Q${cx2},${cy2} ${t.cx},${t.cy}`}
                fill="none"
                stroke={isOn ? '#00d4ff' : 'rgba(0,212,255,0.5)'}
                strokeWidth={isOn ? 1.5 : 0.7}
                strokeDasharray={isOn ? 'none' : '5 5'}
                markerEnd={isOn ? 'url(#bg-arr-on)' : 'url(#bg-arr)'}
              />
              {isOn && (
                <circle r="2.5" fill="#00d4ff" opacity="0.9">
                  <animateMotion dur="0.9s" repeatCount="1"
                    path={`M${f.cx},${f.cy} Q${cx2},${cy2} ${t.cx},${t.cy}`} />
                </circle>
              )}
            </g>
          )
        })}

        {/* Nodes */}
        {NODES.map(n => {
          const { cx, cy } = pos(n)
          return (
            <g key={n.id} transform={`translate(${cx-NW/2},${cy-NH/2})`}>
              <rect width={NW} height={NH} rx="7"
                fill="none"
                stroke="rgba(0,212,255,1)"
                strokeWidth="0.8"
              />
              <text x={NW/2} y={NH/2-3} textAnchor="middle"
                fill="rgba(0,212,255,1)" fontSize="10" fontWeight="600"
                fontFamily="'JetBrains Mono',monospace">{n.label}</text>
              <text x={NW/2} y={NH/2+10} textAnchor="middle"
                fill="rgba(0,212,255,0.7)" fontSize="8"
                fontFamily="'JetBrains Mono',monospace">{n.sub}</text>
            </g>
          )
        })}
      </svg>
    </div>
  )
}
